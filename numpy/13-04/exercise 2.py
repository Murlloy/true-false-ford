import numpy as np

array = np.array([12, 15, 20, 25, 30, 35, 40])

print(f"A Soma Total da Array é: {np.sum(array)}")
print(f"A Média da Array é: {np.mean(array):.2f}")
print(f"A Desvio Padrão da Array é: {np.std(array):.2f}")
print(f"O Numero Maximo da Array é: {np.max(array)}")
print(f"O Numero Minimo da Array é: {np.min(array)}")
